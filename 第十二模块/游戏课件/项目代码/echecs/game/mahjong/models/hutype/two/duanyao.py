    def is_this_type(self, hand_card, card_analyse):
        used_card_type = [CardType.WAN]  # 此游戏中使用的花色
        union_card = hand_card.union_card_info
        for i, count in enumerate(union_card[CardType.WAN]):
            if i == 0 and count != 14:
                return False
            if i == 1 and count != 0:
                return False
            if i == 9 and count != 0:
                return False
        return True


