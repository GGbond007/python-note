# 编程思想/范式

# 面向过程的编程思想：
#      核心是"过程"二字，过程即流程，指的是做事的步骤：先什么、再什么、后干什么
#      基于该思想编写程序就好比在设计一条流水线

# 优点：复杂的问题流程化、进而简单化
# 缺点：扩展性非常差

# 面向过程的编程思想应用场景解析：
# 1、不是所有的软件都需要频繁更迭：比如编写脚本
# 2、即便是一个软件需要频繁更迭，也不并不代表这个软件所有的组成部分都需要一起更迭

